// Helper functions
export const getPackageById = (id: string): TravelPackage | undefined => {
  return mockPackages.find(pkg => pkg.id === id);
};

export const getUserById = (id: string): User | undefined => {
  return mockUsers.find(user => user.id === id);
};

export const getBookingsWithDetails = (): (Booking & { user?: User; package?: TravelPackage })[] => {
  return mockBookings.map(booking => ({
    ...booking,
    user: getUserById(booking.userId),
    package: getPackageById(booking.packageId),
  }));
};

export const getFeaturedPackages = (): TravelPackage[] => {
  return mockPackages.filter(pkg => pkg.isFeatured && pkg.isActive);
};

export const getActivePackages = (): TravelPackage[] => {
  return mockPackages.filter(pkg => pkg.isActive);
};
